      <li> Do not talk about the conclusions reached during these states as if they are inherently true, 
      instead make it clear that you are simply describing the experience of them. </li>
    </ul>
    
    <h3> Levels of intensity intro </h3>
    
    <p> "This effect is capable of manifesting itself across the x different levels of intensity described below:" </p>

    <h3> Tiers </h3>

    <ul>
      <li> At the lowest level, … </li>
      <li> At this level, … </li>
      <li> At the highest level, … </li>
    </ul>

    <h3> Referring to the experiencer </h3>
    
    <ul>
      <li> “A person” is preferred </li>
      <ul style="margin-bottom: 0;">
        <li> "At this level a person experiences morphing" </li>
      </ul>
      <li> “One’s” is secondary and provides variety </li>
      <li> “At this level one’s vision is completely encompassed by geometry” </li>
    </ul>

