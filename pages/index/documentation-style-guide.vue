    <h3> Word preferences: </h3>
    <ul>
      <li> To describe a given environment in its entirety: <i> Scene </i> </li>
      <li> To describe the surface of a given object (like the surface of a wall in a video game): <i> Texture </i> </li>
      <li> To describe a given object: <i> Object </i> </li>
      <li> To describe the experience of an effect of an illusion: <i> Perception </i> </li>
      <li> To describe increasing intensity across multiple tiers: <i> Progressive </i> </li>
      <li> To describe common sober experiences (to contrast with intoxicated effects): <i> Everyday (life) </i> </li>
      <li> To describe an illusion the subject recognizes to be false: <i> Hallucination / hallucinatory </i> </li>
      <li> To describe an illusion the subject cannot recognize as such: <i> Delusion </i> </li>
      <li> To describe the lower bound of an effect range: <i> Subtle </i> </li>
      <li> To describe the upper bound of an effect range: <i> Extreme </i> </li>
      <li> When to use "the trip": use "the trip" or "the [substance] trip" when referring to a specific trip in narrative, or when a kind of experience given specific parameters is being discussed. Example: "After describing the LSD trip Hofmann had experienced, the lecture hall giggled." </li>
      <li> Do not use "and/or", it can always be replaced with more specific language that is more readable. </li>
    </ul>
